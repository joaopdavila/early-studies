# Exercise 1: Accept numbers from a user
# Write a program to accept two numbers from the user and calculate multiplication

n1 = int(input("\nInsira o primeiro número da soma: "))
n2 = int(input("\nInsira o segundo número da soma: "))

mult = n1 * n2

print("\nOs números %d e %d, quando multiplicados, resultam em: %d" % (n1, n2, mult))